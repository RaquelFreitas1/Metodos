package dio.br.metodos;

public class Calculadora {

    public static void soma (double numero1, double numero2){

        double resultado;

        resultado = numero1 + numero2;

        System.out.println("Resultado da soma é: " + resultado);

    }

    public static void subtracao (double numero1, double numero2){

        double resultado;

        resultado = numero1 - numero2;

        System.out.println("Resultado da subtracao é: " + resultado);

    }
    public static void multiplicacao (double numero1, double numero2){

        double resultado;

        resultado = numero1 * numero2;

        System.out.println("Resultado da multiplicacao é: " + resultado);

    }
    public static void divisao (double numero1, double numero2){

        double resultado;

        resultado = numero1 / numero2;

        System.out.println("Resultado da divisao é: " + resultado);

    }


}

